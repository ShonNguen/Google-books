export class Book {
    constructor(
        title,
        authors,
        thumbnail,
        publisher,
        publishedDate,
        categories,
        description
    ) {
        this.title = title; 
        this.authors = authors; 
        this.thumbnail = thumbnail; 
        this.publisher = publisher; 
        this.publishedDate = publishedDate; 
        this.categories = categories; 
        this.description = description;
    }
}